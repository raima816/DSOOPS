package com.raima.week10_L40;
public class numberPattern {
    public static void main(String[] args) {
        int n=4;
        int m=5;

        for(int i=1; i<=n; i++){
            for(int j=1; j<=m; j++){
                System.out.print(j+" ");
            }
            System.out.println();
        }
    }
}
